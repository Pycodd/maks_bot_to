class BotResponses:
    """
    Класс со всеми текстовыми ответами бота.
    """
    @staticmethod
    def registration_start(user_name: str) -> str:
        """
        Приветственное сообщение для начала регистрации.

        Args:
            user_name: Имя пользователя

        Returns:
            str: Текст приветствия
        """
        return (
            f"👋 Здравствуйте, **{user_name}**!\n\n"
            f"Для того чтобы активировать Бота **'Защита'**, "
            f"Вам необходимо пройти регистрацию.\n\n"
            f"📍 **Выберите свой район:**"
        )

    @staticmethod
    def user_agreement() -> str:
        """
        Пользовательское соглашение для бота 'Защита'.

        Returns:
            str: Текст пользовательского соглашения в HTML-формате
        """
        return (
            "<b>ПОЛЬЗОВАТЕЛЬСКОЕ СОГЛАШЕНИЕ</b>\n"
            "<i>Max-бота «Защита» от 01.07.25</i>\n\n"
            "Используя Бота, Вы соглашаетесь с условиями Соглашения.\n\n"
            "📌 <b>Основные положения:</b>\n"
            "1. Бот предназначен для оповещения о сработках пожарной сигнализации.\n"
            "2. Для использования Бота необходима регистрация (ФИО, адрес, телефон).\n"
            "3. Ваши данные используются только для работы Бота и не передаются третьим лицам.\n"
            "4. Полный текст Соглашения доступен по ссылке: \n\n"
            "✅ Нажимая «Принять», Вы подтверждаете согласие с условиями."
        )



